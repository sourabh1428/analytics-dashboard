// ─── Types ────────────────────────────────────────────────────────────────────

type TextSection = { type: "text"; heading: string; rawHeading: string; body: string };
type FaqSection = { type: "faq"; heading: string; rawHeading: string; pairs: FaqPair[] };
type Section = TextSection | FaqSection;

// ─── Content parser ───────────────────────────────────────────────────────────

function cleanHeading(raw: string): string {
  // Strip "Section N — " or "Section N: " prefix generated by the prompt template
  return raw.replace(/^section\s+\d+\s*[—–:-]+\s*/i, "").trim();
}

function parseFaqPairs(body: string): FaqPair[] {
  const pairs: FaqPair[] = [];
  const blocks = body.split(/\n(?=\*\*Q[:\s])/);
  for (const block of blocks) {
    const m = block.match(/^\*\*Q[^*]*?:\s*([\s\S]+?)\*\*\s*\n([\s\S]+)/);
    if (m) pairs.push({ q: m[1].replace(/\*+$/, "").trim(), a: m[2].trim() });
  }
  return pairs;
}

function parseContent(markdown: string): Section[] {
  // Remove a leading duplicate H1 if the model echoed it as a ## heading
  const raw = markdown.replace(/^## [^\n]+\n/, "");
  const chunks = raw.split(/(?=^## )/m).filter((s) => s.trim());

  return chunks.map((chunk): Section => {
    const nl = chunk.indexOf("\n");
    const rawHeading = chunk.slice(0, nl === -1 ? undefined : nl).replace(/^## /, "").trim();
    const heading = cleanHeading(rawHeading);
    const body = nl === -1 ? "" : chunk.slice(nl + 1).trim();

    if (/faq|frequently asked/i.test(rawHeading)) {
      const pairs = parseFaqPairs(body);
      if (pairs.length > 0) return { type: "faq", heading, rawHeading, pairs };
    }

    return { type: "text", heading, rawHeading, body };
  });
}

